@router.post("/ask", summary="Ask a question to the Knowledge Base")
async def ask_question(
    request: AskQuestionRequest,
    db: AsyncSession = Depends(get_db),
    current_user=Depends(get_current_user),
):
    """
    Main Q&A endpoint. Full pipeline:
      1. Semantic search for relevant document chunks
      2. Gemini 2.5 Flash generates a grounded answer
      3. Answer + source references saved to DB

    Request body:
        - question:   The natural language question (min 5 chars)
        - session_id: Optional — pass to continue an existing chat session.
                      If omitted, a new session is created automatically.

    Returns:
        QA message with answer, source chunks, and session info.
    """
    service = QAService(db)
    role_name = current_user.role.name if current_user.role else "viewer"
    # Allow unpublished access if admin AND request explicitly asks for it
    is_privileged = role_name in ["admin"]
    allow_unpublished = is_privileged and (request.allow_unpublished or False)
    message = await service.ask_question(
        request,
        user_id=current_user.id,
        allow_unpublished=allow_unpublished,
    )
    return ResponseBuilder.success(
        data=QAMessageOut.model_validate(message).model_dump(mode="json"),
        message="Answer generated successfully.",
        status_code=201,
    )


@router.get("/ask/stream", summary="Ask with real-time streaming response")
async def ask_question_stream(
    question: str = Query(..., min_length=5, description="Your question"),
    db: AsyncSession = Depends(get_db),
    current_user=Depends(get_current_user),
):
    """
    Streaming version of the Q&A endpoint.
    Returns text/event-stream — tokens are sent as they are generated by Gemini.
    The answer is NOT saved to the database in this mode (use /ask for persistence).

    Query params:
        - question: The natural language question

    Returns:
        Server-Sent Events (SSE) stream of partial answer tokens.
    """
    from app.ai.ai_agent import stream_question

    async def event_generator():
        try:
            async for token in stream_question(question):
                # SSE format: "data: <token>\n\n"
                yield f"data: {token}\n\n"
            yield "data: [DONE]\n\n"
        except Exception as exc:
            yield f"data: [ERROR] {str(exc)}\n\n"

    return StreamingResponse(
        event_generator(),
        media_type="text/event-stream",
        headers={
            "Cache-Control": "no-cache",
            "X-Accel-Buffering": "no",
        },
    )


