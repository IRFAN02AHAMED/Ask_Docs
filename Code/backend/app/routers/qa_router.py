@router.post("/sessions", summary="Create a new Q&A session")
async def create_session(
    data: SessionCreate,
    db: AsyncSession = Depends(get_db),
    current_user=Depends(get_current_user),
):
    """
    Creates a new chat session for organising related questions.

    Request body:
        - title: Optional session name (default: 'New Session')

    Returns:
        Created session record.
    """
    service = QAService(db)
    session = await service.create_session(data, user_id=current_user.id)
    return ResponseBuilder.success(
        data=SessionOut.model_validate(session).model_dump(mode="json"),
        message="Session created successfully.",
        status_code=201,
    )


@router.get("/sessions", summary="List user's Q&A sessions")
async def list_sessions(
    page:       int           = Query(default=1,    ge=1),
    page_size:  int           = Query(default=10,   ge=1, le=100),
    search:     Optional[str] = Query(default=None),
    sort_by:    str           = Query(default="created_at"),
    sort_order: str           = Query(default="desc", pattern="^(asc|desc)$"),
    db: AsyncSession = Depends(get_db),
    current_user=Depends(get_current_user),
):
    """Returns the authenticated user's Q&A sessions, paginated."""
    service = QAService(db)
    items, total = await service.list_sessions(
        current_user.id, page, page_size, search, sort_by, sort_order
    )
    out = [SessionListOut.model_validate(s).model_dump(mode="json") for s in items]
    return ResponseBuilder.paginated(items=out, total=total, page=page, page_size=page_size)


