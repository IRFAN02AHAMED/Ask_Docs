@router.get("/history", summary="Q&A history")
async def get_qa_history(
    page:              int            = Query(default=1,    ge=1),
    page_size:         int            = Query(default=10,   ge=1, le=100),
    search:            Optional[str]  = Query(default=None, description="Search in question text"),
    helpful:           Optional[bool] = Query(default=None),
    validation_status: Optional[str]  = Query(default=None, pattern="^(pending|approved|rejected)$"),
    document_id:       Optional[UUID] = Query(default=None),
    category_id:       Optional[UUID] = Query(default=None),
    user_id:           Optional[UUID] = Query(default=None),
    sort_by:           str            = Query(default="created_at"),
    sort_order:        str            = Query(default="desc", pattern="^(asc|desc)$"),
    db: AsyncSession = Depends(get_db),
    current_user: User = Depends(get_current_user),
):
    """
    Q&A history.

    Admin:
      - Can see all users' Q&A history.
      - Can optionally filter by user_id.

    Normal user/viewer:
      - Can only see their own Q&A history.
      - Any user_id passed from frontend is ignored.
    """

    service = QAService(db)

    role_name = current_user.role.name if current_user.role else "viewer"

    if role_name in ["admin"]:
        effective_user_id = user_id
    else:
        effective_user_id = current_user.id

    items, total = await service.list_all_messages(
        page,
        page_size,
        search,
        helpful,
        validation_status,
        document_id,
        category_id,
        effective_user_id,
        sort_by,
        sort_order,
    )

    out = [
        QAMessageListOut.model_validate(m).model_dump(mode="json")
        for m in items
    ]

    return ResponseBuilder.paginated(
        items=out,
        total=total,
        page=page,
        page_size=page_size,
    )


